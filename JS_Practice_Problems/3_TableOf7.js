// Print the Multiplication Table of 7

// Approach 1 - Using For Loop
console.log("Printing Using For Loop");
for (let i = 1; i < 11; i++) {
    console.log(`7 x ${i} = ${7 * i}`);
}
console.log();

// Approach 2 - Using While Loop
console.log("Printing Using While Loop");
let i = 1;
while (i < 11) {
    console.log(`7 x ${i} = ${7 * i}`);
    i++;
}
console.log();